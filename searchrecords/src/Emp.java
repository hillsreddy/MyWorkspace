//package org.students;
public class Emp {
	String eno;
	String ename;
	Integer salary;
	
	public void setEno(String eno) {
		this.eno = eno;
	}
	public String getEno() {
		return eno;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getEname() {
		return ename;
	}
	public void setSalary(Integer salary) {
		this.salary = salary;
	}
	public Integer getSalary() {
		return salary;
	}
}	